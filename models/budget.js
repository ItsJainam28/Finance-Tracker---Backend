const mongoose = require('mongoose');

// For Budget schema we will the following fields:
// userId: ObjectId
// amount: Number   
// startDate 
//EndDate


const budgetSchema = new mongoose.Schema({
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    amount: {
        type: Number,
        required: true
    },
    startDate: {
        type: Date,
        required: true
    },
    endDate: {
        type: Date,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
})

const Budget = mongoose.model('Budget', budgetSchema);

module.exports = Budget;